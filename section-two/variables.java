public class variables {
    // There are multiple different types of variables
    // Each variable must have the type next to it on initisation and declaration (int num = 4)
    // int: numbers from the range of -2147483648 to 2147483648 (4 bytes)
    // long: numbers that would be above/below int's range (8 bytes)
    // double: numbers with decimals (can reach up to 15) (8 bytes)
    // string: standard string datatype for text
    // follows the lowercamelcase standard (int passengers = 4, int bigCats = 10)
    public static void main(String[] args) {
        int passengers = 5;
        System.out.println(passengers);
    }
}
